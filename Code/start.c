/**
 * [s_print 显示字符]
 * @param O_X    [字符x轴位置，同CCW_PR1()]
 * @param O_Y    [字符y轴位置，同CCW_PR1()]
 * @param s      [字符点阵数据]
 * @param width  [字符宽度]
 * @param height [字符高度]
 */
void s_print(char O_X,byte O_Y,byte s[],uint32 width,uint32 height)
{
	unsigned char count1;
	unsigned int i,j;
	uint32 a;
	a=(width+7)/8;
	i = PARA;	
	j = i*O_Y+O_X+0x04ce;   		//显示一区所控制的显示屏行数 如为320240屏k=j*O_Y+O_X+0x04ce
	WriteCode(0x4f);   				//CSRDIR代码(下移)  
	
	for(i=0;i<a;i++)
	{
		WriteCode(0x46);      		//CSRW 代码
		WriteData(j%256);     		//设置光标地址CSR
		WriteData(j/256);
		WriteCode(0x42);    		//MWRITE 代码 
		
		for(count1=0;count1<height;count1++)
		{
			WriteData(s[i+a*count1]); //写入数据 
		}
		j=j+1;
	}

}                               


